package com.java.code.model;

import java.util.Date;

public class student {
    private long id;
    private String name;
    private Date time;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getTime() {
        return time;
    }

    public void setTime(String name) {
        this.time = time;
    }
}
